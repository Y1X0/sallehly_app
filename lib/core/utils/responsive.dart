/// عدد أعمدة الشبكة المناسب حسب عرض الشاشة.
///
/// على كل الهواتف (العرض أقل من 600) يبقى العدد الأساسي [base] كما هو تماماً
/// — لا تغيير إطلاقاً على أي هاتف صغير أو كبير. فقط على الشاشات الأعرض
/// (أجهزة لوحية) تُضاف أعمدة إضافية بدل بطاقات متمدّدة بعرض غير متناسق.
int responsiveColumns(double width, {int base = 2}) {
  if (width >= 900) return base + 2;
  if (width >= 600) return base + 1;
  return base;
}
